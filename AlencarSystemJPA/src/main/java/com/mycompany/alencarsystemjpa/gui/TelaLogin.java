    private void BotaoLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoLogarActionPerformed
        String login = CampoUsuario.getText().toString();
        String senha = CampoSenha.getText().toString();
        
        Usuario u = new Usuario();
        u.setLogin(login);
        u.setSenha(senha);
        
        u = UsuarioJPA.validarUsuario(u);
        
        if (u != null) {
            JOptionPane.showMessageDialog(null, "Bem vindo(a) " + u.getLogin()+  "! Sua função é de " + u.getNivel() + "!");
            ConsultaLista tela = new ConsultaLista(u);
            tela.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Usuário não encontrado. Tente novamente!");
        }

        dispose();

    }//GEN-LAST:event_BotaoLogarActionPerformed

